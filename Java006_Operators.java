
public class Java006_Operators 
{
    public static void main(String[] args) 
    {
        //Java Operators
        
        /*
        Arithmetic, Assignment, Coparison, Logical, Bitwise operators
        */
        
        //Arithmetic Operators : +, -, *, /,|, ++, --
        //Assignment Operators : =, +=, -=, *=, /=, %=, &=, !=, ^=, >>=, <<=
        //Coparison Operators: ==, !=, >, <, <=, >=
        //Logical Operators: &&, ||, !
        
        int var15 = 200;
        int var16 = 600;
        System.out.println(var15+var16);//800
        System.out.println(var15-var16);//-400
        System.out.println(var15*var16);//120000
        System.out.println(var15/var16);//0
        System.out.println(var15|var16);//728
    }
}
