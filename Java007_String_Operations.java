
public class Java007_String_Operations 
{
    public static void main(String[] args)
    {
        String var17 = "Greetings Java learner";
        String var18 = "James";
        String var19 = "Gosling";
        System.out.println(var17.length());//22
        System.out.println(var17.toUpperCase());
        System.out.println(var17.toLowerCase());
        System.out.println(var18 + " " + var19);
        System.out.println(var18.concat(var19));
    }
}
